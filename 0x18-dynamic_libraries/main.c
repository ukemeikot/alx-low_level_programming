#include "main.h"

/**
 * add - add two numbers
 * @a: num1
 * @b: num2
 * Return: sum of a and b
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * mul - muliply two numbers
 * @a: num1
 * @b: num2
 * Return: a x b
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide two numbers
 * @a: num1
 * @b: num2
 * Return: a / b
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * sub - subtract two numbers
 * @a: num1
 * @b: num2
 * Return: a - b
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mod - find remainder of two numbers
 * @a: num1
 * @b: num2
 * Return: a % b
 */

int mod(int a, int b)
{
	return (a % b);
}
